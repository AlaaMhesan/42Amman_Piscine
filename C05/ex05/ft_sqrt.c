int	ft_sqrt(int nb)
{
	int	num;

	num = 2;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb >= 2)
	{
		while (num * num <= nb)
		{
			if (num * num == nb)
				return (num);
			num++;
		}
	}
	return (0);
}
